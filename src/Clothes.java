public class Clothes {
    private int weight;
    private String color;
    private int area;

    public Clothes(int weight, String color, int area) {
        this.weight = weight;
        this.color = color;
        this.area = area;
    }

    public int getWeight() {
        return weight;
    }

    public String getColor() {
        return color;
    }

    public int getArea() {
        return area;
    }
}
